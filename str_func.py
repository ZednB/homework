def capitalize():
  """Функция принимает слово
    и возвращает заглавными буквами"""
  word = input("Введите слово ")
  return word.upper()

print(capitalize())

def capitalize2():
  """Функция принимает слово
  и делает первую букву заглавной"""
  word2 = input("Введите слово ")
  return word2.capitalize()

print(capitalize2())
